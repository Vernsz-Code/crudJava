    private void btnSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSimpanMouseClicked
        // TODO add your handling code here:
        String nama, kelas, pelajaran;
        
        if(namaTxt.getText().equals("") || kelasTxt.getText().equals("") || pelajaranTxt.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Data Harus Di Isi Semua!!!");
        }else{
            nama = namaTxt.getText();
            kelas = kelasTxt.getText();
            pelajaran = pelajaranTxt.getText();
            create(nama, kelas, pelajaran);
            namaTxt.setText("");
            kelasTxt.setText("");
            pelajaranTxt.setText("");
            JOptionPane.showMessageDialog(null, "Data Berhasil DiTambahkan");
        }
    }//GEN-LAST:event_btnSimpanMouseClicked

