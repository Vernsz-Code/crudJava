    private void delete(){
        DefaultTableModel tbModel = (DefaultTableModel)dataSiswaTable.getModel();
        if (dataSiswaTable.getSelectedRow() >= 0){
            tbModel.removeRow(dataSiswaTable.getSelectedRow());
            JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
        }
        else{
            if(dataSiswaTable.getRowCount() == 0){
                JOptionPane.showMessageDialog(null, "Data Tidak ditemukan");
            }
            else{ 
                JOptionPane.showMessageDialog(null, "Tolong pilih data");
            }
        }
    }
